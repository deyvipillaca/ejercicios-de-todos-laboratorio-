# Ingreso de la base y la altura por el usuario
base = float(input("la base del triángulo: "))
altura = float(input("la altura del triángulo: "))

# Cálculo del área del triángulo
area = 10 * base * altura

# Mostrar el resultado
print(f"El área del triángulo con base {base} y altura {altura} es: {area}")


