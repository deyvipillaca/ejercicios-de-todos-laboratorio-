# Crear una lista con al menos 6 nodos
lista_original = [1, 2, 3, 4, 5, 6]

# Invertir el orden de la lista
lista_invertida = lista_original[::-1]

# Imprimir la lista invertida hacia adelante
print("Lista invertida hacia adelante:", lista_invertida)

# Imprimir la lista invertida hacia atrás
print("Lista invertida hacia atrás:", lista_invertida[::-1])

