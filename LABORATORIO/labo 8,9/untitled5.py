# Definir dos objetos con el mismo valor
objeto1 = "Hola"
objeto2 = "Hola"

# Validar la igualdad utilizando el operador ==
if objeto1 == objeto2:
    print("Los objetos son iguales utilizando el operador ==")

# Validar la igualdad utilizando la función is
if objeto1 is objeto2:
    print("Los objetos son iguales utilizando la función is")
